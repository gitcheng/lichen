import numpy as np

################################################################################
# Gaussian (normal function)
################################################################################
def pdf_gaussian():
    ret = lambda p, x: (p[0]/p[2])*np.exp(-((x - p[1])**2)/(2.0*p[2]*p[2]))
    return ret

################################################################################
# Chi^2 minimization function.
################################################################################

def chi2_function(fit_function):
    ret = lambda p, x, y: fit_function(p, x)-y
    return ret

